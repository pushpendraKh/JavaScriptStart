// Unary Operator
var a = 1;
console.log(a++) // Prints 1 and increment by 1 -> Postfix Increment
console.log(++a) // Prints 3...  prefix increment

// Binary Operator
var a = 3;
var b = 4;
let c = a + b;
console.log(c);

// Tertiary Operator 
let condition = false
let output = condition ? a : b
console.log(output)


// Equally
console.log(3=='3') // True, checks the value

// Strict Equally
console.log(3==='3') // False